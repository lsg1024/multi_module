-- 테넌트별 SENS API 설정
CREATE TABLE SENS_CONFIG (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TENANT_ID VARCHAR(10) NOT NULL UNIQUE,
    ACCESS_KEY VARCHAR(255) NOT NULL,
    SECRET_KEY VARCHAR(255) NOT NULL,
    SERVICE_ID VARCHAR(255) NOT NULL,
    SENDER_PHONE VARCHAR(15) NOT NULL,
    ENABLED BOOLEAN DEFAULT TRUE NOT NULL,
    CREATE_DATE TIMESTAMP,
    LAST_MODIFIED_DATE TIMESTAMP
);

-- 메시지 전송 이력
CREATE TABLE MESSAGE_HISTORY (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TENANT_ID VARCHAR(10) NOT NULL,
    RECEIVER_PHONE VARCHAR(15) NOT NULL,
    RECEIVER_NAME VARCHAR(50),
    CONTENT TEXT NOT NULL,
    STATUS VARCHAR(20) NOT NULL,
    ERROR_MESSAGE VARCHAR(500),
    NAVER_REQUEST_ID VARCHAR(100),
    SENT_BY VARCHAR(50) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT NOW()
);

CREATE INDEX idx_msg_history_tenant ON MESSAGE_HISTORY(TENANT_ID);
CREATE INDEX idx_msg_history_created ON MESSAGE_HISTORY(CREATED_AT);
