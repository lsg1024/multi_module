    order_product_id              BIGINT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id                      BIGINT          UNIQUE,                           -- 1:1
    product_id                    BIGINT,                                            -- product FK (수동 보관)
    product_name                  VARCHAR(255),                                      -- denorm
    product_factory_name          VARCHAR(255),                                      -- denorm
    product_size                  VARCHAR(255),                                      -- 주문 인스턴스 고유 값
    is_product_weight_sale        BOOLEAN         DEFAULT FALSE,
    gold_weight                   NUMERIC(10, 3),                                    -- 인스턴스 실측치
    stone_weight                  NUMERIC(10, 3),                                    -- 인스턴스 실측치
    order_main_stone_note         TEXT,
    order_assistance_stone_note   TEXT,
    product_purchase_cost         INTEGER,                                           -- freeze
    product_labor_cost            INTEGER,                                           -- freeze (등급별)
    product_add_labor_cost        INTEGER,                                           -- freeze
    material_id                   BIGINT,                                            -- denorm (lookup)
    material_name                 VARCHAR(255),                                      -- denorm
    classification_id             BIGINT,                                            -- denorm
    classification_name           VARCHAR(255),                                      -- denorm
    color_id                      BIGINT,                                            -- denorm
    color_name                    VARCHAR(255),                                      -- denorm
    set_type_id                   BIGINT,                                            -- denorm
    set_type_name                 VARCHAR(255),                                      -- denorm
    assistant_stone               BOOLEAN         NOT NULL DEFAULT FALSE,            -- 보조석 사용 여부
    assistant_stone_id            BIGINT,                                            -- assistance_stone FK
    assistant_stone_name          VARCHAR(255),                                      -- denorm
    assistant_stone_create_at     TIMESTAMP,
    CONSTRAINT fk_order_product_orders
        FOREIGN KEY (order_id) REFERENCES orders (order_id) ON DELETE CASCADE
);

CREATE INDEX idx_order_product_product   ON order_product (product_id);
CREATE INDEX idx_order_product_assistant ON order_product (assistant_stone_id);

