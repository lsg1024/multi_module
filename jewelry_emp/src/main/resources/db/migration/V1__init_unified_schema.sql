    stock_note                     TEXT,
    stock_main_stone_note          TEXT,
    stock_assistance_stone_note    TEXT,
    stone_main_labor_cost          INTEGER,                                           -- freeze (등급별)
    stone_assistance_labor_cost    INTEGER,                                           -- freeze
    stone_add_labor_cost           INTEGER,
    total_stone_purchase_cost      INTEGER,
    total_stone_labor_cost         INTEGER,
    stock_deleted                  BOOLEAN         NOT NULL DEFAULT FALSE,
