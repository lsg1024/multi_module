-- ====================================================================
-- jewelry_emp Monolith Unified Schema (V1 baseline)
--
-- Target  : PostgreSQL (cloud DB)
-- Profile : per-tenant schema (SchemaMultiTenantConnectionProvider 가
--           각 tenant 스키마를 검색 경로로 설정한 뒤 Flyway 가 본 SQL 을 적용)
-- Source  : multi_module 4 개 모듈(account / product / order / user) 의
--           모든 @Entity / @Embeddable 정의를 정확히 따른 통합 베이스라인.
--
-- 정규화 노트
--   - ProductSnapshot @Embeddable 은 stock 테이블에 그대로 인라인된다
--     (현재 엔티티 그대로 유지). 컬럼 의미를 중복(=denorm)/freeze(=time-frozen)
--     으로 구분해 주석에 표시했으니, 후속 정리에서 denorm 컬럼만 제거하기 쉽게 했다.
--   - orders / stock / sale 의 store_*/factory_*/account_* 컬럼 중
--     *_harry, *_grade, *_gold_price 는 거래 시점 freeze 값이라 유지.
--     *_name 은 denorm(향후 FK join 으로 대체 가능).
--   - Hibernate 의 CamelCaseToUnderscoresNamingStrategy 와
--     PostgreSQL 의 unquoted-identifier-folding 규칙에 맞추어 모든 식별자는 lower_snake_case.
-- ====================================================================

-- ====================================================================
-- 0. 안전 재실행을 위한 cleanup (기존 placeholder 잔재가 있을 때만 의미 있음)
--    Flyway 는 V1 을 한 번만 실행하지만, 새 스키마에 다른 잔재가 있을 수 있어
--    DROP 을 명시적으로 두지 않는다 (이미 적용된 경우 V2 로 변경분 이행).
-- ====================================================================

