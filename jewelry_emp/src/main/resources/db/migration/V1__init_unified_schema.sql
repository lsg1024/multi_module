CREATE INDEX idx_orders_factory       ON orders (factory_id);
CREATE INDEX idx_orders_status        ON orders (order_status);
CREATE INDEX idx_orders_create_at     ON orders (create_at);

-- 주문 상품 (Orders 와 1:1, FK 측 보유. audit 미상속)
