-- 주문 (2026-05 P4: BaseEntity 상속, denorm 컬럼 제거)
CREATE TABLE orders (
    order_id          BIGINT             GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    flow_code         BIGINT             NOT NULL,                        -- TSID, 주문/재고/판매 공통 추적키
    store_id          BIGINT,                                              -- account.store FK (이름은 storeFinder 조회)
    store_grade       VARCHAR(255),                                        -- freeze (계약 시점 등급)
    store_harry       NUMERIC(10, 2),                                      -- freeze (계약 시점 환율)
    factory_id        BIGINT,                                              -- account.factory FK (이름은 factoryFinder 조회)
    factory_harry     NUMERIC(10, 2),                                      -- freeze
    order_note        TEXT,
    -- 도메인 시각 컬럼 (사용자 입력 주문 접수일·출고일)
    create_at         TIMESTAMP,
    shipping_at       TIMESTAMP,
    order_deleted     BOOLEAN            NOT NULL DEFAULT FALSE,
    priority_id       BIGINT,
    product_status    VARCHAR(30)        NOT NULL,                         -- enum ProductStatus
    order_status      VARCHAR(30)        NOT NULL,                         -- enum OrderStatus
    -- BaseEntity 상속 audit (Orders.createAt 도메인 컬럼과 별개 의미)
    create_date       TIMESTAMP,
    last_modified_date TIMESTAMP,
    created_by        VARCHAR(255),
    last_modified_by  VARCHAR(255),
    CONSTRAINT fk_orders_store
        FOREIGN KEY (store_id) REFERENCES store (store_id),
    CONSTRAINT fk_orders_factory
        FOREIGN KEY (factory_id) REFERENCES factory (factory_id),
    CONSTRAINT fk_orders_priority
        FOREIGN KEY (priority_id) REFERENCES priority (priority_id)
);

CREATE INDEX idx_orders_flow_code     ON orders (flow_code);
