-- 경비 관리: 통장구분
CREATE TABLE IF NOT EXISTS EXPENSE_BANK_TYPE (
    EXPENSE_BANK_TYPE_ID    BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    BANK_TYPE_NAME          VARCHAR(100) NOT NULL,
    BANK_TYPE_NOTE          VARCHAR(255),
    DELETED                 BOOLEAN NOT NULL DEFAULT FALSE,

    CREATE_DATE             TIMESTAMP NOT NULL DEFAULT NOW(),
    LAST_MODIFIED_DATE      TIMESTAMP NOT NULL DEFAULT NOW(),
    CREATED_BY              VARCHAR(255),
    LAST_MODIFIED_BY        VARCHAR(255)
);

-- 경비 관리: 수입계정
CREATE TABLE IF NOT EXISTS EXPENSE_INCOME_ACCOUNT (
    EXPENSE_INCOME_ACCOUNT_ID   BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    INCOME_ACCOUNT_NAME         VARCHAR(100) NOT NULL,
    INCOME_ACCOUNT_NOTE         VARCHAR(255),
    DELETED                     BOOLEAN NOT NULL DEFAULT FALSE,

    CREATE_DATE                 TIMESTAMP NOT NULL DEFAULT NOW(),
    LAST_MODIFIED_DATE          TIMESTAMP NOT NULL DEFAULT NOW(),
    CREATED_BY                  VARCHAR(255),
    LAST_MODIFIED_BY            VARCHAR(255)
);

-- 경비 관리: 지출계정
CREATE TABLE IF NOT EXISTS EXPENSE_EXPENSE_ACCOUNT (
    EXPENSE_EXPENSE_ACCOUNT_ID  BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    EXPENSE_ACCOUNT_NAME        VARCHAR(100) NOT NULL,
    EXPENSE_ACCOUNT_NOTE        VARCHAR(255),
    DELETED                     BOOLEAN NOT NULL DEFAULT FALSE,

    CREATE_DATE                 TIMESTAMP NOT NULL DEFAULT NOW(),
    LAST_MODIFIED_DATE          TIMESTAMP NOT NULL DEFAULT NOW(),
    CREATED_BY                  VARCHAR(255),
    LAST_MODIFIED_BY            VARCHAR(255)
);

-- 경비 관리: 경비 기록
CREATE TABLE IF NOT EXISTS EXPENSE_RECORD (
    EXPENSE_RECORD_ID           BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    RECORD_DATE                 TIMESTAMP NOT NULL,
    EXPENSE_TYPE                VARCHAR(20) NOT NULL,
    EXPENSE_BANK_TYPE_ID        BIGINT,
    EXPENSE_INCOME_ACCOUNT_ID   BIGINT,
    EXPENSE_EXPENSE_ACCOUNT_ID  BIGINT,
    COUNTERPARTY                VARCHAR(100),
    DESCRIPTION                 VARCHAR(500),
    MATERIAL                    VARCHAR(50),
    WEIGHT                      NUMERIC(10, 3),
    QUANTITY                    INTEGER DEFAULT 1,
    UNIT_PRICE                  BIGINT DEFAULT 0,
    SUPPLY_AMOUNT               BIGINT DEFAULT 0,
    TAX_AMOUNT                  BIGINT DEFAULT 0,
    DELETED                     BOOLEAN NOT NULL DEFAULT FALSE,

    CREATE_DATE                 TIMESTAMP NOT NULL DEFAULT NOW(),
    LAST_MODIFIED_DATE          TIMESTAMP NOT NULL DEFAULT NOW(),
    CREATED_BY                  VARCHAR(255),
    LAST_MODIFIED_BY            VARCHAR(255),

    CONSTRAINT FK_EXPENSE_RECORD_BANK_TYPE
        FOREIGN KEY (EXPENSE_BANK_TYPE_ID) REFERENCES EXPENSE_BANK_TYPE(EXPENSE_BANK_TYPE_ID),
    CONSTRAINT FK_EXPENSE_RECORD_INCOME_ACCOUNT
        FOREIGN KEY (EXPENSE_INCOME_ACCOUNT_ID) REFERENCES EXPENSE_INCOME_ACCOUNT(EXPENSE_INCOME_ACCOUNT_ID),
    CONSTRAINT FK_EXPENSE_RECORD_EXPENSE_ACCOUNT
        FOREIGN KEY (EXPENSE_EXPENSE_ACCOUNT_ID) REFERENCES EXPENSE_EXPENSE_ACCOUNT(EXPENSE_EXPENSE_ACCOUNT_ID)
);

-- 인덱스
CREATE INDEX IDX_EXPENSE_RECORD_DATE ON EXPENSE_RECORD (RECORD_DATE);
CREATE INDEX IDX_EXPENSE_RECORD_TYPE ON EXPENSE_RECORD (EXPENSE_TYPE);
CREATE INDEX IDX_EXPENSE_RECORD_DATE_TYPE ON EXPENSE_RECORD (RECORD_DATE, EXPENSE_TYPE);
